#include<bits/stdc++.h>
#define gc getchar
#define pc putchar
#define pii pair<int,int>
using namespace std;
inline void rdl(int &x)
{
	x=0;
	bool check=false;
	register int c;
	do{
		c=gc();
		if(c=='-')
		check=true;
	}while(c<48 || c>57);
	for(;c>47 && c<58;c=gc()){
		x=(x<<1)+(x<<3)+c-48;
	}
	if(check)
	x=-x;
}
inline void rd(int &x)
{
	x=0;
	bool check=false;
	register int c=gc();
	for(;c<48 || c>57;c=gc());
	for(;c>47 && c<58;c=gc()){
		x=(x<<1)+(x<<3)+c-48;
	}

}
inline void pd(int x)
{
	char c[11];
	bool check=false;
	int k=-1;
	if(x<0)
	check=true,x=-x;
	do
	{
		c[++k]=x%10+48;
		x/=10;
	}while(x);
	if(check)
	c[++k]='-';
	while(k>=0)
	pc(c[k--]);
	
}
inline void pdl(long long int x)
{
	char c[21];
	bool check=false;
	int k=-1;
	if(x<0)
	check=true,x=-x;
	do
	{
		c[++k]=x%10+48;
		x/=10;
	}while(x);
	if(check)
	c[++k]='-';
	while(k>=0)
	pc(c[k--]);
	pc('\n');
}
bool cmp(int  a,int b)
{
	return a>b;
}
bool visited[101];
int main()
{
	int t,n,m,k,i,j;
	int cnt,cnt1;
	rd(t);
	while(t--)
	{
		cnt=0;
		
		rd(n);
		fill(visited,visited+n+1,false);
		rd(m);
		cnt1=n-m;
		rd(k);
		for(i=0;i<m;i++)
		rd(j),visited[j]=true;
		for(i=0;i<k;i++)
		{
			rd(j);
			if(visited[j])
			{
				cnt++;
				
			}
			else
			cnt1--;
		}
		pd(cnt);
		pc(' ');
		pd(cnt1);
		pc('\n');
	}
}

